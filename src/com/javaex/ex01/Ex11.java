package com.javaex.ex01;

public class Ex11 {
	
	public static void main(String[] args) {
		
		//관계연산자
		int a = 5;
		int b = 3;
		
		
		System.out.println(a < b);
		System.out.println(a > b);
		
		System.out.println(a <= b);
		System.out.println(a >= b);
		
		System.out.println(a == b); // 대입, 주입 같니?
		System.out.println(a != b); // a랑 b랑 다르니?
		
		boolean result = a>b;
		System.out.println(result);
		
		
		
	}
	

}
